    override fun onMapReady(googleMap: GoogleMap) {
        mMap = googleMap
        try {
            // Customise the styling of the base map using a JSON object defined
            // in a raw resource file.
            val success = googleMap.setMapStyle(
                MapStyleOptions.loadRawResourceStyle(this, R.raw.gta_style)
            )
            if (!success) {
                Log.e(TAG, "Style parsing failed.")
            }
        } catch (e: Resources.NotFoundException) {
            Log.e(TAG, "Can't find style. Error: ", e)
        }
        // Position the map's camera near Sydney, Australia.
        //googleMap.moveCamera(CameraUpdateFactory.newLatLng(LatLng(-34, 151)))

        val bengaluru = LatLng(12.9716, 77.5946)
        mMap.addMarker(MarkerOptions().position(bengaluru).title("Marker in Bengaluru"))
        mMap.moveCamera(CameraUpdateFactory.newLatLng(bengaluru))
    }
